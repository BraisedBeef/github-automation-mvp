/**
 * 用于验证域名的正则表达式。
 *
 * 该正则表达式支持:
 * - 字母和数字字符 (a-z, A-Z, 0-9)
 * - 中文字符 (Unicode 范围 \u4e00-\u9fa5)
 * - 域名中的连字符 (-)（但不能在开头或结尾）
 * - 域名长度在 1 到 63 个字符之间
 * - 顶级域名 (TLD) 至少包含 2 个字母字符
 * - 中文顶级域名至少包含 2 个字符
 * - CN域名 以**.cn结尾 (例如 `example.hk.cn`)
 */
export const DOMAIN_REG =
  /^(?:[a-z0-9\u00A0-\uFFFF](?:[a-z0-9-\u00A0-\uFFFF]{0,61}[a-z0-9\u00A0-\uFFFF])?\.)+[a-z0-9-\u00A0-\uFFFF]{2,}$/i

/**
 * 用于泛域名验证域名的正则表达式。
 *
 * 该正则表达式支持:
 * - 可选的通配符子域名 (例如 `*.example.com`)
 * - 域名中的字母、数字、连字符和中文字符
 * - 域名段长度在 1 到 63 个字符之间
 * - 顶级域名 至少包含 2 个字母字符或中文字符
 * - CN域名 以**.cn结尾 (例如 `example.hk.cn`)
 */
export const DOMAIN_WI_REG =
  /^(?:\*\.)?(?:[a-z0-9\u00A0-\uFFFF](?:[a-z0-9-\u00A0-\uFFFF]{0,61}[a-z0-9\u00A0-\uFFFF])?\.)+[a-z0-9-\u00A0-\uFFFF]{2,}$/i

// E-mail地址
export const EMAIL_REG = /^\w+([\.\-]\w+)*\@\w+([\.\-]\w+)*\.\w+$/
