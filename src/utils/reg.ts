  /^(?![\d]+$)(?![a-z]+$)(?![A-Z]+$)(?![!?~@><.,|)(\\/「」+=\-\[\]\{\}#$%^&*]+$)[\da-zA-z!?~@><.,|)(\\/「」+=\-\[\]\{\}#$%^&*]{6,20}$/
